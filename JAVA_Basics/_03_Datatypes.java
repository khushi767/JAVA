/*
 Datatypes: Primitive, Non Primitive Datatype
 Non Primitive Datatype: Strings, Arrays, Objects, etc.
 Primitive Datatype: Boolean, Numeric: character , Integral
 INteger: integer, floating
 Integer:byte,short,int,long
 floating: float,double

 */

public class Datatypes_03 {
    public static void main(String args[]){
        int a=5;
        System.out.println(a);

        int yournumber=67;
        System.out.println(yournumber);

        boolean isveg=false;
        System.out.println(isveg);

        float myfloat=5;
        System.out.println(myfloat);

        float myyfloat=5.0f;
        System.out.println(myyfloat);

        String wishes="happy";
        System.out.println(wishes);

    }
}


